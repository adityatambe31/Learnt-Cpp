// compersion operator helps in comparing two integers which conists of >, <, >=, <=, !=...........

#include<iostream>
using namespace std;
int main()
{
    int a = 20;
    int b = 10;

    cout<<(a>b)<<endl;
    cout<<(a<b)<<endl;
    cout<<(a>=b)<<endl;
    cout<<(a<=b)<<endl;
    cout<<(a==b)<<endl;
    cout<<(a!=b)<<endl;

return 0;
}