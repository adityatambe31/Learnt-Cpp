// assignment operator helps in using the operators with the predefined variables.....


#include<iostream>
using namespace std;
int main()
{
    int a = 10;

    a+=10;
    cout<<a<<endl;

    a-=10;
    cout<<a<<endl;
    
    a*=10;
    cout<<a<<endl;
    
    a/=10;
    cout<<a<<endl;
    
    a%=10;
    cout<<a<<endl;

return 0;
}