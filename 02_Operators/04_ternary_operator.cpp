// Ternary Operator
// ? : , in other words short hand if else one line to be precise.....


#include<iostream>
using namespace std;
int main()
{
    int a = 10;
    int b = 20;
    int c = 30;
    int d = 5;

    (a > b) ? cout<<"A is greater!!"<<endl : cout<<"B is greater!!"<<endl;;
    (c > d) ? cout<<"C is greater!!"<<endl : cout<<"D is greater!!"<<endl;;
}